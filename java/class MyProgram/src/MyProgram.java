public class MyProgram {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(7 % 3); // %는 나머지 1을 출력
        System.out.println(3 % 7); // %는 나머지 3을 출력
    }
}
